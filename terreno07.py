'''Desenvolva um algoritimo que receba como entrada as medidas de um terreno, calcule e
exiba sua área.'''

# entrada
largura =  float(input('Informe a largura do terreno:'));
profundidade = float(input('Informe a profundidade do terreno:'));

# processamento
area = largura * profundidade;

# saída
print('A área do terreno é:',area);
