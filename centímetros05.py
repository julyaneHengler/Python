'''Desenvolva um algoritimo que receba um valor em emtros e converta para centímetros, depois
apresente o resultado em centímetros.'''

# entrada
metros = float(input('Informe o valor em metros:'));

# processamento
centimetros = metros * 100;

# saída
print('O valor',metros, 'em metros, convertido para centímetros é:',centimetros);
